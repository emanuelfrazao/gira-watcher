<h1>Sobre o GIRA Watch</h1>
<p>
	GIRA Watch e um projeto civico open-source que monitoriza a disponibilidade das estacoes de
	bicicletas GIRA em Lisboa, criando um dataset transparente e reprodutivel para responsabilizacao publica.
</p>
